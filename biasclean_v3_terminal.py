    def _prepare_features(self, df: pd.DataFrame, feature_map: Dict[str, str],
                          target_column: str, group_column: Optional[str] = None) -> Tuple[pd.DataFrame, np.ndarray, np.ndarray]:
        """
        Prepare features for SVM training with proper leakage prevention.
        Protected attributes are EXCLUDED from features to prevent direct bias.

        Args:
            df: Input DataFrame (BiasClean corrected data)
            feature_map: Mapping of protected features to column names
            target_column: Target outcome variable
            group_column: Protected attribute for fairness monitoring (optional)

        Returns:
            Tuple of (feature_matrix, target_labels, group_labels)
        """
        X = df.copy()

        # Step 1: Extract and remove target variable (CRITICAL: prevents data leakage)
        if target_column not in X.columns:
            raise ValueError(f"Target column '{target_column}' not found in DataFrame")
        y = X[target_column].astype(int).values
        X = X.drop(columns=[target_column])

        # Step 2: Extract group labels for fairness monitoring (DO NOT retain as feature)
        group_labels = None
        protected_columns = []

        if group_column and group_column in X.columns:
            group_labels = X[group_column].astype(str).values

        # Step 3: Identify ALL protected attribute columns from feature_map for exclusion
        for protected_feature, column_name in feature_map.items():
            if column_name in X.columns and column_name not in protected_columns:
                protected_columns.append(column_name)

        # Step 4: Identify and remove problematic columns
        exclude_cols = []

        # ================================
        # EDUCATION DOMAIN LEAKAGE FIX
        # ================================
        # In OULAD, y_true is derived directly from final_result (Pass/Fail/Withdrawn/Distinction)
        # If final_result stays in X, SVM sees the label proxy → 100% accuracy (leakage).
        if 'final_result' in X.columns:
            exclude_cols.append('final_result')


        # Remove identifier columns
        id_patterns = ['_id', 'id_', 'name', 'date', 'time', 'timestamp', 'case_number']
        for col in X.columns:
            col_lower = col.lower()
            if any(pattern in col_lower for pattern in id_patterns):
                exclude_cols.append(col)

        # Remove high-cardinality string columns (>50 unique values)
        for col in X.select_dtypes(include=['object']).columns:
            if col not in exclude_cols and X[col].nunique() > 50:
                exclude_cols.append(col)

        # Remove COMPAS-specific leakage features
        leakage_patterns = [
            'decile', 'score_text', 'c_charge_degree', 'r_charge_degree',
            'v_score_text', 'type_of_assessment', 'v_type_of_assessment',
            'two_year_recid', 'violent_recid', 'is_violent_recid',
            'vr_charge_degree', 'v_decile_score', 'event', 'r_days_from_arrest',
            # NEW ADDITIONS CRITICAL FOR FAIRNESS:
            'svm_',  # Excludes svm_margin, svm_y_pred, etc.
            'event', 'end', 'start',  # Time-based leakage
            'priors_count', 'juv_',  # Direct recidivism predictors
            'charge_',  # Charge-related leakage
            '_date', '_time'  # Temporal data
        ]
        for col in X.columns:
            col_lower = col.lower()
            if any(pattern in col_lower for pattern in leakage_patterns):
                exclude_cols.append(col)

        # Remove date columns that encode outcome timing
        date_patterns = ['_date', '_in', '_out', 'screening', 'custody']
        for col in X.columns:
            col_lower = col.lower()
            if any(pattern in col_lower for pattern in date_patterns):
                exclude_cols.append(col)

        # Remove criminal history features that directly predict recidivism
        criminal_history = ['priors_count', 'juv_fel_count', 'juv_misd_count',
                          'juv_other_count', 'priors_count.1']
        for col in criminal_history:
            if col in X.columns:
                exclude_cols.append(col)

        # CRITICAL FIX: Remove SVM-related columns that shouldn't exist yet
        # (These would be leftovers from previous runs or test data issues)
        svm_leakage_patterns = ['svm_', '_margin', '_prob', '_pred', 'margin_']
        for col in X.columns:
            col_lower = col.lower()
            if any(pattern in col_lower for pattern in svm_leakage_patterns):
                if col not in exclude_cols:
                    exclude_cols.append(col)

        # Remove time-based leakage columns (duplicate check for safety)
        time_patterns = ['event', 'end', 'start', '_date', '_time', 'timestamp']
        for col in X.columns:
            col_lower = col.lower()
            if any(pattern in col_lower for pattern in time_patterns):
                if col not in exclude_cols:
                    exclude_cols.append(col)

        # CRITICAL FIX: Exclude ALL protected attribute columns from features
        for protected_col in protected_columns:
            if protected_col not in exclude_cols and protected_col in X.columns:
                exclude_cols.append(protected_col)

        # Step 5: Create clean feature matrix (without protected attributes)
        X_features = X.drop(columns=[c for c in set(exclude_cols) if c in X.columns])

        # Step 6: One-hot encode categorical variables
        X_features = pd.get_dummies(X_features, drop_first=False, dtype=int)

                # Step 7: Handle missing and invalid values
        X_features = X_features.replace([np.inf, -np.inf], np.nan)
        X_features = X_features.fillna(0)

        # ============ STEP 1: LEAKAGE CHECK ============
        print(f"\n   DEBUG: Checking for leakage columns in features")
        print(f"   Current feature count: {X_features.shape[1]}")

        leakage_cols = ['y_pred', 'y_prob', 'final_result', 'group', 'y_true', 'biasclean_target']
        found_leakage = []

        for col in leakage_cols:
            if col in X_features.columns:
                found_leakage.append(col)
                print(f"   ⚠️  CRITICAL: Found leakage column '{col}' in features")

        if found_leakage:
            print(f"   Removing leakage columns: {found_leakage}")
            X_features = X_features.drop(columns=found_leakage)

        print(f"   Clean feature count: {X_features.shape[1]}")
        # ============ END LEAKAGE CHECK ============

        # Store feature columns for later reference
        self.feature_columns = X_features.columns.tolist()

        return X_features, y, group_labels

    def enforce_fairness(self, df: pd.DataFrame, feature_map: Dict[str, str],
                         target_column: str, group_column: Optional[str] = None,
                         validation_split: float = 0.25) -> Dict[str, Any]:
        """
        Apply SVM fairness enforcement to corrected dataset.

        Args:
            df: BiasClean corrected DataFrame
            feature_map: Protected feature mappings
            target_column: Target outcome variable
            group_column: Primary protected attribute for monitoring
            validation_split: Proportion for validation holdout (0.0-0.5)

        Returns:
            Dictionary containing predictions, metrics, and enhanced DataFrame
        """
        print(f"\n{'='*80}")
        print("SVM FAIRNESS ENFORCEMENT")
        print(f"{'='*80}")

        # Prepare features with leakage prevention
        X_features, y_true, group_labels = self._prepare_features(
            df, feature_map, target_column, group_column
        )

        print(f"   Samples: {len(df):,}")
        print(f"   Features: {X_features.shape[1]}")
        print(f"   Target: '{target_column}' (positive rate: {y_true.mean():.1%})")

        if group_column:
            print(f"   Protected attribute: '{group_column}' (excluded from features)")

        # Train SVM with fairness-aware configuration
        print(f"\n   Training SVM with fairness-aware configuration...")
        self.model = Pipeline([
            ("scaler", StandardScaler(with_mean=True, with_std=True)),
            ("svm", SVC(**self.svm_config))
        ])

        if validation_split > 0:
            # Train-validation split
            X_train, X_val, y_train, y_val = train_test_split(
                X_features, y_true,
                test_size=validation_split,
                random_state=self.random_state,
                stratify=y_true
            )

            # ADD THIS FOR SPEED: Subsample training data
            if len(X_train) > 5000:
                # Reset indices to avoid alignment issues
                X_train_reset = X_train.reset_index(drop=True)
                y_train_reset = pd.Series(y_train).reset_index(drop=True)

                # Sample indices
                sample_indices = np.random.choice(len(X_train_reset), 5000, replace=False)

                X_train = X_train_reset.iloc[sample_indices]
                y_train = y_train_reset.iloc[sample_indices].values
                print(f"   Subsample: Using 5,000 samples for SVM training")

            print(f"   Training samples: {len(X_train):,}")
            print(f"   Validation samples: {len(X_val):,}")

            # Train on training split
            self.model.fit(X_train, y_train)

            # Evaluate on validation set
            val_pred = self.model.predict(X_val)
            val_acc = (val_pred == y_val).mean()
            print(f"   Validation accuracy: {val_acc:.1%}")

            # Generate predictions on full dataset for fairness enforcement
            y_pred = self.model.predict(X_features).astype(int)
            y_prob = self.model.predict_proba(X_features)[:, 1].astype(float)
            full_acc = (y_pred == y_true).mean()

        else:
            # Train on full dataset
            self.model.fit(X_features, y_true)
            y_pred = self.model.predict(X_features).astype(int)
            y_prob = self.model.predict_proba(X_features)[:, 1].astype(float)
            full_acc = (y_pred == y_true).mean()
            print(f"   Note: Training on full dataset (no validation split)")
            val_acc = None

        # Compute decision boundary margins
        margin = self._compute_margin(X_features.values)

        # Calculate fairness metrics
        fairness_metrics = self._calculate_fairness_metrics(
            y_true, y_pred, group_labels, group_column
        )

        # Prepare results dictionary
        results = {
            'svm_predictions': y_pred,
            'svm_probabilities': y_prob,
            'svm_margins': margin,
            'fairness_metrics': fairness_metrics,
            'feature_columns': self.feature_columns,
            'model_config': self.svm_config,
            'sample_count': len(df),
            'validation_split': validation_split,
            'validation_accuracy': val_acc,
            'full_accuracy': full_acc
        }

        # Create enhanced DataFrame with SVM outputs
        df_svm = df.copy()
        df_svm['svm_y_pred'] = y_pred
        df_svm['svm_y_prob'] = y_prob
        df_svm['svm_margin'] = margin

