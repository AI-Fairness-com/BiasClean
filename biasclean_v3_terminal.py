            stage_data = {
                'bias_score': bias_score,
                'records': len(df),
                'timestamp': datetime.now().isoformat()
            }

            # Add confidence intervals if calculator provided
            if confidence_calculator is not None and bias_score is not None:
                # Create array of bias scores for CI calculation
                # (simplified - in real usage this would be from bootstrap samples)
                ci_lower, ci_mean, ci_upper = confidence_calculator.calculate_ci(
                    np.array([bias_score] * 100)  # Placeholder
                )
                stage_data['ci_lower'] = ci_lower
