def json_serializer(obj):
    """
    Custom JSON serializer for numpy types, pandas objects, and other non-serializable objects.
    To be used as default=json_serializer in json.dump() calls.
    """
    # Handle numpy types
    if isinstance(obj, np.integer):
        return int(obj)
    elif isinstance(obj, np.floating):
        return float(obj)
    elif isinstance(obj, np.ndarray):
        return obj.tolist()
    elif isinstance(obj, np.bool_):
        return bool(obj)  # Convert numpy bool to Python bool
    elif isinstance(obj, bool):
        return obj  # Python bool is already JSON serializable

    # Handle pandas/missing values
    elif pd.isna(obj):
        return None
    elif isinstance(obj, pd.Timestamp):
        return obj.isoformat()

    # Handle datetime objects
    elif hasattr(obj, 'isoformat'):
        return obj.isoformat()

    # Handle other common types
    elif isinstance(obj, (pd.Series, pd.DataFrame)):
        return obj.to_dict()
    elif isinstance(obj, set):
        return list(obj)

    # For everything else, use string representation
    try:
        return str(obj)
    except:
        raise TypeError(f"Object of type {type(obj).__name__} is not JSON serializable: {obj}")

