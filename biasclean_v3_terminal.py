class PreMitigationAuditor:
    """
    Audit dataset before any mitigation using v2.7's own methodology.
    Returns traffic-light recommendation.
    """

    def __init__(self, domain_weights: dict):
        self.weights = domain_weights

    def audit(self, df: pd.DataFrame, target: str, feature_map: dict) -> dict:
        """Complete pre-mitigation audit"""
        print(f"\n{'='*80}")
        print("BIASCLEAN v3.0: PRE-MITIGATION AUDIT")
        print(f"{'='*80}")

        results = {
            "data_quality": self._check_data_quality(df, target, feature_map),
            "baseline_fairness": self._measure_fairness(df, target, feature_map),
            "vulnerable_subgroups": self._find_vulnerable_groups(df, target, feature_map),
        }

        results["recommendation"] = self._generate_recommendation(results)

        self._print_summary(results)
        return results

    def _check_data_quality(self, df, target, feature_map) -> dict:
        """v2.7's ConstraintValidator logic"""
        issues = []
        warnings = []

        # Sample size check (v2.7 line 510)
        n = len(df)
        if n < config.THRESHOLDS["min_total_records"]:
            issues.append(f"Insufficient records: {n} < {config.THRESHOLDS['min_total_records']}")

        # Class balance (v2.7 line 546)
        if target in df.columns:
            pos_rate = df[target].mean()
            if not (config.THRESHOLDS["pos_rate_lower"] <= pos_rate <= config.THRESHOLDS["pos_rate_upper"]):
                warnings.append(f"Class imbalance: {pos_rate:.1%} positive")

        # Missing data (v2.7 line 512)
        missing_pct = df.isnull().sum().sum() / (n * len(df.columns))
        if missing_pct > config.THRESHOLDS["max_missing_rate"]:
            issues.append(f"Excessive missing data: {missing_pct:.1%}")

        # Group sizes (v2.7 line 510)
        min_group_sizes = []
        for col in feature_map.values():
            if col in df.columns:
                sizes = df[col].value_counts()
                min_group_sizes.append(sizes.min())

        if min_group_sizes:
            overall_min = min(min_group_sizes)
            if overall_min < config.THRESHOLDS["min_samples_per_group"]:
                warnings.append(f"Small groups: min size = {overall_min}")

        return {
            "blocking_issues": issues,
            "warnings": warnings,
            "n_records": n,
            "missing_pct": missing_pct,
            "quality_ok": len(issues) == 0
        }

    def _measure_fairness(self, df, target, feature_map) -> dict:
        """v2.7's coefficient-of-variation disparity (lines 906-935)"""
        per_feature = {}

        for feature, col in feature_map.items():
            if col not in df.columns:
                continue

            # Calculate group rates (v2.7 line 918: >=10 samples)
            group_rates = {}
            for group in df[col].dropna().unique():
                group_data = df[df[col] == group]
                if len(group_data) >= config.THRESHOLDS["min_group_calc"]:
                    rate = group_data[target].mean()
                    group_rates[str(group)] = rate

            if len(group_rates) < 2:
                continue

            # CV disparity (v2.7 lines 931-932)
            rates = np.array(list(group_rates.values()))
            mean_rate = np.mean(rates)

            if mean_rate == 0:
                cv = 0.0
            else:
                cv = np.std(rates) / mean_rate
                cv = min(cv, 1.0)  # v2.7 cap

            per_feature[feature] = {
                "disparity": float(cv),
                "weight": self.weights.get(feature, 0.05),
                "weighted": cv * self.weights.get(feature, 0.05),
                "n_groups": len(group_rates)
            }

        total_disparity = sum(f["weighted"] for f in per_feature.values())

        return {
            "per_feature": per_feature,
            "aggregate_bias": total_disparity,
            "n_features": len(per_feature)
        }

    def _find_vulnerable_groups(self, df, target, feature_map) -> dict:
        """Identify groups at risk during mitigation"""
        vulnerable = []
        overall_rate = df[target].mean()

        for feature, col in feature_map.items():
            if col not in df.columns:
                continue

            for group in df[col].dropna().unique():
                group_data = df[df[col] == group]
                group_rate = group_data[target].mean()

                risk_factors = []
                if len(group_data) < config.THRESHOLDS["min_samples_per_group"]:
                    risk_factors.append("small_sample")
                if group_rate < overall_rate * 0.8:
                    risk_factors.append("disadvantaged")

                if risk_factors:
                    vulnerable.append({
                        "feature": feature,
                        "group": str(group),
                        "size": len(group_data),
                        "rate": group_rate,
                        "risks": risk_factors
                    })

        return {
            "groups": vulnerable,
            "count": len(vulnerable)
        }

    def _generate_recommendation(self, results: dict) -> dict:
        """Traffic-light recommendation"""
        issues = results["data_quality"]["blocking_issues"]
        bias = results["baseline_fairness"]["aggregate_bias"]
        vulnerable = results["vulnerable_subgroups"]["count"]

        # RED conditions
        if issues:
            return {
                "traffic_light": "🔴 RED",
                "action": "BLOCKED - Address data quality",
                "rationale": issues,
                "proceed": False
            }

        if bias > 0.30:
            return {
                "traffic_light": "🔴 RED",
                "action": "BLOCKED - Severe baseline bias",
                "rationale": [f"Bias score {bias:.3f} > 0.30"],
                "proceed": False
            }

        # YELLOW conditions
        if bias > 0.15 or vulnerable > 0:
            return {
                "traffic_light": "🟡 YELLOW",
                "action": "REVIEW REQUIRED",
                "rationale": [
                    f"Moderate bias: {bias:.3f}" if bias > 0.15 else None,
                    f"Vulnerable groups: {vulnerable}" if vulnerable > 0 else None
                ],
                "proceed": True  # Allow with caution
            }

        # GREEN
        return {
            "traffic_light": "🟢 GREEN",
            "action": "PROCEED WITH MITIGATION",
            "rationale": [f"Acceptable baseline: bias={bias:.3f}, vulnerable={vulnerable}"],
            "proceed": True
        }

    def _print_summary(self, results: dict):
        """Concise audit summary"""
        rec = results["recommendation"]
        print(f"\n  Audit Complete: {rec['traffic_light']}")
        print(f"  Recommendation: {rec['action']}")
        print(f"  Records: {results['data_quality']['n_records']:,}")
        print(f"  Bias Score: {results['baseline_fairness']['aggregate_bias']:.3f}")
        print(f"  Vulnerable Groups: {results['vulnerable_subgroups']['count']}")
        print(f"{'='*80}")

